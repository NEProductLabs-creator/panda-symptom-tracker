// ─── Variant 2: ER visit ──────────────────────────────────────────────────────

function generateERVisitPDF(
  args: GenerateArgs,
  recentLogs: ReturnType<typeof useSymptomLogs>["logs"],
  medications: ReturnType<typeof useMedications>["medications"],
  triggers: ReturnType<typeof useTriggerLog>["entries"],
) {
  const { childName, childAge, visitDate, parentNotes } = args;
  const doc = new jsPDF({ orientation: "portrait", unit: "mm", format: "letter" });
  const margin = 14;
  const pw = doc.internal.pageSize.getWidth();

  // Urgent intake phrase box
  doc.setFillColor(...URGENT);
  doc.rect(0, 0, pw, 50, "F");
  doc.setFont("helvetica", "bold");
  doc.setFontSize(13);
  doc.setTextColor(255, 255, 255);
  const phrase = "My child had a sudden neuropsychiatric change.\nI am concerned about PANS or PANDAS.\nPlease consider an infectious workup.";
  doc.text(phrase, pw / 2, 20, { align: "center", lineHeightFactor: 1.6 });
  doc.setFontSize(8);
  doc.setFont("helvetica", "normal");
  doc.setTextColor(255, 200, 200);
  doc.text(`Date: ${fmtDate(visitDate)}  |  Generated: ${format(new Date(), "MMMM d, yyyy")}`, pw / 2, 45, { align: "center" });

  let y = 58;

  // Patient info
  if (childName || childAge) {
    y = section(doc, "Patient Information", y, margin);
    y += 2;
    doc.setFont("helvetica", "normal");
    doc.setFontSize(9);
    doc.setTextColor(40);
    if (childName) { doc.text(`Name: ${childName}`, margin, y); y += 5; }
    if (childAge) { doc.text(`Age: ${childAge}`, margin, y); y += 5; }
    y += 2;
  }

  // Current symptoms — most recent log
  const mostRecent = [...recentLogs].sort((a, b) => b.date.localeCompare(a.date))[0];
  y = maybeNewPage(doc, y, 60);
  y = section(doc, `Current Symptoms${mostRecent ? ` (logged ${fmtDate(mostRecent.date)})` : ""}`, y, margin, URGENT);
  y += 1;
  if (mostRecent) {
    const CAT_ROWS: [string, number | null | undefined, string][] = [
      ["OCD / Intrusive Thoughts", mostRecent.ocd, "0 = none, 5 = extreme"],
      ["Anxiety", mostRecent.anxiety, "0 = none, 5 = extreme"],
      ["Rage / Irritability", mostRecent.rage, "0 = none, 5 = extreme"],
      ["Tics", mostRecent.tics, "0 = none, 5 = extreme"],
      ["Sleep Quality", mostRecent.sleep, "5 = excellent, 0 = very poor"],
      ["Cognitive Function", mostRecent.cognition, "5 = excellent, 0 = very poor"],
    ];
    autoTable(doc, {
      startY: y,
      head: [["Symptom Category", "Score", "Scale"]],
      body: CAT_ROWS.map(([cat, score, scale]) => [
        cat,
        score !== null && score !== undefined ? `${score}/5` : "Not logged",
        scale,
      ]),
      margin: { left: margin, right: margin },
      styles: { fontSize: 9, cellPadding: 3 },
      headStyles: { fillColor: [...URGENT] as [number, number, number], textColor: 255, fontStyle: "bold", fontSize: 8.5 },
      columnStyles: {
        0: { cellWidth: 60 },
        1: { cellWidth: 18, halign: "center", fontStyle: "bold" },
        2: { cellWidth: "auto" },
      },
    });
    y = (doc as any).lastAutoTable.finalY + 4;
    if (mostRecent.notes) {
      y = bodyText(doc, `Parent notes from ${fmtDate(mostRecent.date)}: ${mostRecent.notes}`, y, margin, 8.5);
      y += 2;
    }
  } else {
    y = bodyText(doc, "No symptom log available. Please describe current symptoms verbally.", y, margin);
  }

  // Current medications
  const activeMeds = medications.filter((m) => !m.endDate || m.endDate >= visitDate);
  y = maybeNewPage(doc, y, 50);
  y = section(doc, "Current Medications", y, margin, URGENT);
  y += 1;
  if (activeMeds.length === 0) {
    y = bodyText(doc, "No current medications recorded.", y, margin);
    y += 2;
  } else {
    autoTable(doc, {
      startY: y,
      head: [["Medication", "Dose", "Started"]],
      body: activeMeds.map((m) => [
        m.name,
        `${m.dose}${m.frequency ? `, ${m.frequency}` : ""}`,
        fmtDate(m.startDate),
      ]),
      margin: { left: margin, right: margin },
      styles: { fontSize: 8.5, cellPadding: 3 },
      headStyles: { fillColor: [...URGENT] as [number, number, number], textColor: 255, fontStyle: "bold", fontSize: 8 },
      columnStyles: { 0: { cellWidth: 60 }, 1: { cellWidth: "auto" }, 2: { cellWidth: 28 } },
    });
    y = (doc as any).lastAutoTable.finalY + 6;
  }

  // Recent illness & exposure (last 30 days of triggers)
  const thirtyDaysAgo = format(subDays(new Date(), 30), "yyyy-MM-dd");
  const recentTriggers = triggers.filter((t) => t.date >= thirtyDaysAgo);
  y = maybeNewPage(doc, y, 50);
  y = section(doc, "Recent Illness & Exposure (Last 30 Days)", y, margin, URGENT);
  y += 1;
  if (recentTriggers.length === 0) {
    y = bodyText(doc, "No illness or exposure triggers logged in the last 30 days.", y, margin);
    y += 2;
  } else {
    autoTable(doc, {
      startY: y,
      head: [["Date", "Type", "Notes"]],
      body: recentTriggers
        .sort((a, b) => b.date.localeCompare(a.date))
        .map((t) => [fmtDate(t.date), t.category.replace(/_/g, " "), t.notes ?? ""]),
      margin: { left: margin, right: margin },
      styles: { fontSize: 8.5, cellPadding: 3 },
      headStyles: { fillColor: [...URGENT] as [number, number, number], textColor: 255, fontStyle: "bold", fontSize: 8 },
      columnStyles: { 0: { cellWidth: 24 }, 1: { cellWidth: 42 }, 2: { cellWidth: "auto" } },
    });
    y = (doc as any).lastAutoTable.finalY + 6;
  }

  if (parentNotes.trim()) {
    y = maybeNewPage(doc, y, 30);
    y = section(doc, "Parent / Caregiver Notes", y, margin, URGENT);
    y += 2;
    y = bodyText(doc, parentNotes.trim(), y, margin);
  }

  pageFooters(doc, margin);
  const slug = childName ? childName.replace(/\s+/g, "-").toLowerCase() : "child";
  doc.save(`pans-er-visit-${slug}-${format(parseISO(visitDate + "T00:00:00"), "yyyyMMdd")}.pdf`);
}

// ─── Variant 3: Follow-up with PANS provider ──────────────────────────────────

function generateFollowUpPDF(
  args: GenerateArgs,
  logs30: ReturnType<typeof useSymptomLogs>["logs"],
  medications: ReturnType<typeof useMedications>["medications"],
) {
  const { childName, childAge, visitDate, parentNotes } = args;
  const doc = new jsPDF({ orientation: "portrait", unit: "mm", format: "letter" });
  const margin = 14;
  const pw = doc.internal.pageSize.getWidth();

  // Header
  doc.setFillColor(...HEAD);
  doc.rect(0, 0, pw, 36, "F");
  doc.setFont("helvetica", "bold");
  doc.setFontSize(17);
  doc.setTextColor(255, 255, 255);
  doc.text("PANS & PANDAS Symptom Report", margin, 13);
  doc.setFont("helvetica", "normal");
  doc.setFontSize(9);
  doc.setTextColor(200, 230, 200);
  if (childName) doc.text(`${childName}${childAge ? ` · Age ${childAge}` : ""}`, margin, 21);
  doc.text(`Visit date: ${fmtDate(visitDate)}`, margin, 27);
  doc.text(`Generated: ${format(new Date(), "MMMM d, yyyy")}`, pw - margin, 27, { align: "right" });
  doc.setFontSize(8);
  doc.setTextColor(170, 210, 170);
  doc.text("Follow-up with PANS provider — last 30 days", margin, 33);

  let y = 44;

  // Category averages
  const CAT_KEYS = ["ocd", "anxiety", "rage", "tics", "sleep", "cognition"] as const;
  const CAT_LABELS: Record<typeof CAT_KEYS[number], string> = {
    ocd: "OCD / Intrusive Thoughts", anxiety: "Anxiety", rage: "Rage / Irritability",
    tics: "Tics", sleep: "Sleep Quality (5=excellent)", cognition: "Cognitive / Focus (5=excellent)",
  };
  const sorted = [...logs30].sort((a, b) => b.date.localeCompare(a.date));
  const catStats = CAT_KEYS.map((k) => {
    const vals = sorted.map((l) => l[k]).filter((v): v is number => v !== null && v !== undefined);
    const avg = vals.length ? vals.reduce((s, v) => s + v, 0) / vals.length : 0;
    const worst = vals.length ? Math.max(...vals) : 0;
    return { key: k, label: CAT_LABELS[k], avg, worst };
  });

  y = section(doc, `Symptom Category Averages (${sorted.length} logged days)`, y, margin);
  y += 1;
  autoTable(doc, {
    startY: y,
    head: [["Symptom Category", "Avg Score", "Worst Day"]],
    body: catStats.map(({ label, avg, worst }) => [
      label,
      avg > 0 ? avg.toFixed(1) : "—",
      worst > 0 ? `${worst}/5` : "—",
    ]),
    margin: { left: margin, right: margin },
    styles: { fontSize: 8.5, cellPadding: 2.8, valign: "middle" },
    headStyles: { fillColor: [80, 110, 80], textColor: 255, fontStyle: "bold", fontSize: 8 },
    columnStyles: {
      0: { cellWidth: 70 },
      1: { cellWidth: 24, halign: "center", fontStyle: "bold" },
      2: { cellWidth: 24, halign: "center" },
    },
  });
  y = (doc as any).lastAutoTable.finalY + 6;

  // 30-day log
  y = maybeNewPage(doc, y, 55);
  y = section(doc, "Daily Symptom Log (last 30 days)", y, margin);
  y += 1;
  if (sorted.length === 0) {
    y = bodyText(doc, "No symptom logs recorded in the last 30 days.", y, margin);
  } else {
    autoTable(doc, {
      startY: y,
      head: [["Date", "OCD", "Anx", "Rage", "Tics", "Sleep", "Cog", "Notes"]],
      body: sorted.map((l) => [
        fmtDate(l.date),
        l.ocd ?? "—", l.anxiety ?? "—", l.rage ?? "—", l.tics ?? "—",
        l.sleep ?? "—", l.cognition ?? "—",
        l.notes ? l.notes.slice(0, 55) + (l.notes.length > 55 ? "…" : "") : "",
      ]),
      margin: { left: margin, right: margin },
      styles: { fontSize: 8, cellPadding: 2.5 },
      headStyles: { fillColor: [80, 110, 80], textColor: 255, fontSize: 7.5 },
      columnStyles: {
        0: { cellWidth: 22 },
        1: { cellWidth: 9, halign: "center" }, 2: { cellWidth: 9, halign: "center" },
        3: { cellWidth: 9, halign: "center" }, 4: { cellWidth: 9, halign: "center" },
        5: { cellWidth: 9, halign: "center" }, 6: { cellWidth: 9, halign: "center" },
        7: { cellWidth: "auto" },
      },
    });
    y = (doc as any).lastAutoTable.finalY + 6;
  }

  // Medications
  const thirtyDaysAgo = format(subDays(new Date(), 30), "yyyy-MM-dd");
  const activeMeds = medications.filter((m) => !m.endDate || m.endDate >= thirtyDaysAgo);
  if (activeMeds.length > 0) {
    y = maybeNewPage(doc, y, 50);
    y = section(doc, "Current & Recent Medications", y, margin);
    y += 1;
    autoTable(doc, {
      startY: y,
      head: [["Medication", "Dose", "Started", "Ended"]],
      body: activeMeds.map((m) => [
        m.name, m.dose, fmtDate(m.startDate), m.endDate ? fmtDate(m.endDate) : "Ongoing",
      ]),
      margin: { left: margin, right: margin },
      styles: { fontSize: 8.5, cellPadding: 3 },
      headStyles: { fillColor: [80, 110, 80], textColor: 255, fontStyle: "bold", fontSize: 8 },
      columnStyles: { 0: { cellWidth: 55 }, 1: { cellWidth: 35 }, 2: { cellWidth: 25 }, 3: { cellWidth: 25 } },
    });
    y = (doc as any).lastAutoTable.finalY + 6;
  }

  if (parentNotes.trim()) {
    y = maybeNewPage(doc, y, 30);
    y = section(doc, "Parent / Caregiver Notes", y, margin);
    y += 2;
    y = bodyText(doc, parentNotes.trim(), y, margin);
  }

  pageFooters(doc, margin);
  const slug = childName ? childName.replace(/\s+/g, "-").toLowerCase() : "child";
  doc.save(`pans-follow-up-${slug}-${format(parseISO(visitDate + "T00:00:00"), "yyyyMMdd")}.pdf`);
}

