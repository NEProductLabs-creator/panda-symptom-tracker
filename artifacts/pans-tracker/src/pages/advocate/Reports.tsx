// ─── PDF helpers ─────────────────────────────────────────────────────────────

function fmtDate(d: string) {
  try { return format(parseISO(d + "T12:00:00"), "MMM d, yyyy"); } catch { return d; }
}

function section(doc: jsPDF, title: string, y: number, margin: number, color = HEAD): number {
  const pw = doc.internal.pageSize.getWidth();
  doc.setFillColor(...color);
  doc.rect(margin, y, pw - margin * 2, 7, "F");
  doc.setFont("helvetica", "bold");
  doc.setFontSize(9);
  doc.setTextColor(255, 255, 255);
  doc.text(title.toUpperCase(), margin + 3, y + 4.8);
  doc.setTextColor(0);
  return y + 9;
}

function maybeNewPage(doc: jsPDF, y: number, needed = 40): number {
  if (y > doc.internal.pageSize.getHeight() - needed) { doc.addPage(); return 18; }
  return y;
}

function bodyText(doc: jsPDF, text: string, y: number, margin: number, fontSize = 9): number {
  const pw = doc.internal.pageSize.getWidth();
  doc.setFont("helvetica", "normal");
  doc.setFontSize(fontSize);
  doc.setTextColor(40);
  const lines = doc.splitTextToSize(text, pw - margin * 2);
  doc.text(lines, margin, y);
  return y + lines.length * (fontSize * 0.35) + 3;
}

function pageFooters(doc: jsPDF, margin: number): void {
  const total = (doc.internal as unknown as { getNumberOfPages: () => number }).getNumberOfPages();
  for (let i = 1; i <= total; i++) {
    doc.setPage(i);
    const pw = doc.internal.pageSize.getWidth();
    const ph = doc.internal.pageSize.getHeight();
    doc.setFont("helvetica", "normal");
    doc.setFontSize(7);
    doc.setTextColor(160);
    doc.text(`Page ${i} of ${total}`, pw - margin, ph - 6, { align: "right" });
    doc.text("Generated by PANS & PANDAS Tracker", margin, ph - 6);
  }
}

